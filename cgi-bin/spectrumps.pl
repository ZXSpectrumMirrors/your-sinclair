<center><font size=6><b><u><i>The Rules</i></u></b></font></center><p>
<ul>
<li>Both players start with a set number of playing cards each and the aim of the game is to own the lot of them.</li>
<li>The current player at the start of a new game is chosen by tossing a coin.</li>
<li>That player then looks at the first card in his hand and chooses a rating to play.</li>
<li>His opponent looks at the rating corresponding to his own first card. The highest rating wins and the winner gets the two cards that were being played.</li>
<li>If the opponent wins, he takes control of the game, otherwise it's the starting player that begins the next round.</li>
<li>In the event of a draw, the two cards that were being played are placed into a pile and are won by the winner of the next round.</li>
<li>The game ends when a player doesn't have any more cards in his hand.</li>
</ul>

<center><font size=6><b><u><i>The Ratings</i></u></b></font></center><p>
<center>A player can choose between the following ratings for each game:</center>
<ul>
<li><b>Whooshability</b> - Was the game dead fast to play, or was the gameplay of a more sedate nature?</li>
<li><b>Strokability</b> - Did the game charm you with its sickening cuteness or did it have the hugability of Margaret Thatcher?</li>
<li><b>Blastability</b> - Were there plenty of things to shoot, kill, maim or destroy? Or was it a game you could safely play in front of your granny without her tutting?</li>
<li><b>Ponderability</b> - How puzzling was the game to play? Did you have to stop and think every now and then or was it a simple case of shoot first and ask questions later?</li>
<li><b>Memorability</b> - Do people still talk about the game today, a couple of decades since its first release? Or do they rank alongside Brosettes?</li>
</ul>
