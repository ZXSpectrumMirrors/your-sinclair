<TABLE WIDTH=100%><TR><TD VALIGN="TOP"><IMG ALIGN=LEFT WIDTH=200 HEIGHT=190 SRC="globes/advent_globe_1.png" ALT="snowglobe"><font color="yellow"><b><i>MERRY CHRISTMAS!</i></b></font><br>
&nbsp;&nbsp;&nbsp;&nbsp;This Advent Calendar complements the <a href="http://www.ysrnry.co.uk/tvprog/downloads.htm#xmas">Documentary Christmas Special</a> - a forty-minute-long video celebrating the Speccy Christmases of the 1980's, featuring all the big games, the hype, the music and the magazines. Well worth a look, it's viewable on YouTube and also downloadable in high-quality video.<br>
&nbsp;&nbsp;&nbsp;&nbsp;Whilst the documentary hopes to rekindle memories of Christmases gone by, the pre-Christmas hype cycle in particular, this Advent Calendar is a much more personal affair. Not only is it filled with old games and Speccy memories, but also tales from my own childhood Christmases when I was growing up with my Spectrum. I think I've distilled every single memory I have into a few hundred words each day for you to enjoy.<br>
&nbsp;&nbsp;&nbsp;&nbsp;You'll also find a few YouTube videos on each page featuring some of the games, plus old television clips and music videos from the same time-frame. Some of them are a bit off the wall, but hopefully there's something for everybody! If you watched the telly or listened to the radio during the '80s, you might be in for a bit of a surprise.<br>
&nbsp;&nbsp;&nbsp;&nbsp;Naturally, this Advent Calendar comes complete with tacky synthetic pressed chocolates, just like all the others. As a bonus, these are completely fat free!<br>
