	<center><font size=7><b><i><u>The Rules</u></i></b></font></center><p>
	This game is loosely based on "Who Wants To Be A Millionaire?" (<i>very</i> loosely, Mr Celador Lawyer Chappy),
but instead of answering general knowledge questions, you have to identify Spectrum games from their screenshots.<p>
There are ten rounds in all, and you have three lifelines: <b>50/50</b> (removes two of the four answers), <b>Ask</b> Previous Players
(tells you what previous players thought the answer was), and <b>Phone</b> a previous player (same as before, but only tells you one percentage).<p>
Your answers are recorded and used in future lifelines.<p>
